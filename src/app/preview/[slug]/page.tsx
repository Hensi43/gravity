// Deterministic Theme Selection
function getTheme(title: string) {
    if (!title) return THEMES.midnight;

    // Keyword matching overrides
    const lower = title.toLowerCase();
    if (lower.includes('game') || lower.includes('meta') || lower.includes('crypto')) return THEMES.cyber;
    if (lower.includes('dashboard') || lower.includes('saas') || lower.includes('crm')) return THEMES.solar;
    if (lower.includes('news') || lower.includes('blog') || lower.includes('portfolio')) return THEMES.swiss;

    // Fallback deterministic hash
    const sum = title.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
    const themes = Object.values(THEMES);
    return themes[sum % themes.length];
}

